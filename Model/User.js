const mongoose= require('mongoose');

const UserScheme=mongoose.Schema({
    name:{
        type: String,
        required: true
    },
    email:{
        type: String,
        required: true,
        unique: true
    },
    password:{
        type: String,
        required: true
    },
    date:{
        type: Date,
        default: Date.now
    }
})
//now export 
module.exports= mongoose.model('User',UserScheme)